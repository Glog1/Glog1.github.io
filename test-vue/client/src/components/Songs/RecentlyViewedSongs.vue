<template>
    <panel title="Recently" class="mb1">
        <v-data-table
                :headers="headers"
                :pagination.sync="pagination"
                :items="histories">
            <template slot="items" slot-scope="props">
                <td class="text-xs-right">
                    {{props.item.id}}
                </td>
                <td class="text-xs-right">
                    {{props.item.title}}
                </td>
                <td class="text-xs-right">
                    {{props.item.artist}}
                </td>
            </template>
        </v-data-table>
    </panel>
</template>
<script>
    import {mapState} from 'vuex'
    import SongHistoryService from '@/services/SongHistoryService'

export default {
  data () {
        return {
          headers: [
            {
              text: 'id',
              value: 'id'
            },
            {
              text: 'Title',
              value: 'title'
            },
            {
              text: 'Artist',
              value: 'artist'
            }
          ],
          pagination: {
            sortBy: 'createdAt',
            descending: true
          },
          histories: []
        }
  },
  computed: {
        ...mapState([
          'isUserLoggedIn',
          'user'
        ])
  },
  async mounted () {
        if (this.isUserLoggedIn) {
          this.histories = (await SongHistoryService.index({
            userId: this.user.id
          })).data
        }
  }
}
</script>
